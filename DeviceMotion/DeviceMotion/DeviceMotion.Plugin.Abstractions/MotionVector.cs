using System;

namespace DeviceMotion.Plugin
{
	/// <summary>
	/// Motion vector.
	/// </summary>
	public class MotionVector
	{
		/// <summary>
		/// Gets or sets the x.
		/// </summary>
		/// <value>The x.</value>
		public double X { get; set; }
		/// <summary>
		/// Gets or sets the y.
		/// </summary>
		/// <value>The y.</value>
		public double Y { get; set; }
		/// <summary>
		/// Gets or sets the z.
		/// </summary>
		/// <value>The z.</value>
		public double Z { get; set; }
	}
}

